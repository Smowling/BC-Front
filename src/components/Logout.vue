<template>
    <button @click="logout"> Logout </button>
</template>

<script setup>
import { ref } from "vue";
import { supabase } from '../lib/supabaseClient';


async function logout() {
	const { error } = await supabase.auth.signOut();

	if (error) {
		console.log(error);
	}
	else {
		console.log("Sign out success")
	}
}

</script>

